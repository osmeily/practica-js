/** Ejercicio #2
 *  Se requiere conocer el área de un rectángulo. Realice un algoritmo para
 *  tal fin. El área del rectángulo se halla así: (base*altura)
 */

let base = Number(prompt("Ingrese la base del rectángulo:"));
let altura = Number(prompt("Ingrese la altura del rectángulo:"));
let area = base * altura;
alert(`El área del rectágulo es: ${area}`)
